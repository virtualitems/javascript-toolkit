(function(window, document) {
'use strict';

// ----------------------------------------
// Checks
// ----------------------------------------

if (!window.React) {
  console.error('React is not loaded');
  return;
}

if (!window.ReactDOM) {
  console.error('ReactDOM is not loaded');
  return;
}


// ----------------------------------------
// Globals
// ----------------------------------------

const {
  React,
  ReactDOM

} = window;

const {
  createElement: ce,
  Fragment,
  StrictMode

} = React;


// ----------------------------------------
// Errors
// ----------------------------------------



// ----------------------------------------
// Utils
// ----------------------------------------



// ----------------------------------------
// Hooks
// ----------------------------------------



// ----------------------------------------
// Context
// ----------------------------------------



// ----------------------------------------
// Models
// ----------------------------------------



// ----------------------------------------
// Contents
// ----------------------------------------



// ----------------------------------------
// Containers
// ----------------------------------------



// ----------------------------------------
// Composites
// ----------------------------------------



// ----------------------------------------
// Layout
// ----------------------------------------

const Layout = function() {
  return ce(Fragment);
};


// ----------------------------------------
// View
// ----------------------------------------

const View = function() {
  return ce(Fragment, null, ce(Layout));
};


// ----------------------------------------
// Root
// ----------------------------------------

ReactDOM
  .createRoot(document.getElementById('root'))
  .render(ce(StrictMode, null, ce(View)));


})(window, document);
